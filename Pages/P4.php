						<h3>Petit poème aux petits enfants</h3>
												
						<p class="left Poeme">
							Tout dans l'immuable nature <br />
							Est miracle aux petits enfants ;<br />
							Ils naissent, et leur âme obscure<br />
							Eclôt dans des enchantements.<br />
							Le reflet de cette magie<br />
							Donne à leur regard un rayon.<br />
							Déjà la belle Illusion<br />
							Excite leur frêle énergie.<br />
							L'inconnu, l'inconnu divin,<br />
							Les baigne comme une eau profonde ;<br />
							On les presse, on leur parle en vain,<br />
							Ils habitent un autre monde ;<br />
							Leurs yeux purs, leurs yeux grands ouverts,<br />
							S'emplissent de rêves étranges.<br />
							Oh ! qu'ils sont beaux, ces petits anges<br />
							Perdus dans l'antique univers.<br />
							Leur tête légère et ravie<br />
							Songe tandis que nous pensons ;<br />
							Ils font de frissons en frissons<br />
							La découverte de la vie.<br />
							<h6>_____ Anatole France</h6>
