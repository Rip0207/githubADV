						<h3><span class="DateFont">1953</span> Mes études à l’Université du Travail </h3>
						
						<p>
							L’électrotechnique est une partie de ma vie. Tout a commencé en 1949. J’ai débuté mes études d’Ingénieur technicien. 
							J’ai terminé mes études en 1954. J’ai alors été engagé aux ACEC le 2 août 1954, à Charleroi et ai fait une carrière passionnante.
						</p>
						<p>
							Dans le cours de cette carrière, je me suis également attaché à découvrir et comprendre cette petite révolution qu’a été 
							l’électronique. À la fin de mes études, il n’y avait même pas dix ans que le transistor avait été inventé ! J’ai donc suivi 
							toute l’évolution de cette grande aventure.
						</p>
						<p>
							Les ordinateurs d’aujourd’hui étaient impensables à l’époque. Et pourtant me voilà aujourd’hui à l’utiliser quotidiennement. 
							C’est aussi grâce à l’ordinateur que je peux aujourd’hui transmettre mes mémoires.
						</p>
